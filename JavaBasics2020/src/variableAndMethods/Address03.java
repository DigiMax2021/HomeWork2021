package variableAndMethods;

public class Address03 {

	public static void main(String[] args) {
		//static command is not required on each line below for variables called under Main STATIC Method above

		//declaration of variable below
		String Name = "Tofael"; //no static required
		int age = 44; // no static required
		char sex = 'M'; //no static required as variable declared under Main Static Method above
		double grade = 3.876; //no static required
	
		//implementation of variables below
			
			System.out.println(Name); 
			System.out.println(age);
			System.out.println(sex);		
			System.out.println(grade);
			
			System.out.println("My Name:  " +Name + ", My Age: " +age+ ", My Sex: " + sex + ", My Grade: " +grade);
		
	}

}
